// Arithimetics operations
let a = 10; 
let b = 3; 
 
console.log(a + b);    // 13 
console.log(a - b);    // 7 
console.log(a * b);    // 30 
console.log(a / b);    // 3.333... 
console.log(a % b);    // 1 
console.log(a ** b);   // 1000 

// compound assignments
let x = 5;
x += 2; // x = 7
x *= 3; // x = 21

// Increments & Decrements:
let y = 1;
y++;  // y = 2
++y;  // y = 3
y--;  // y = 2
