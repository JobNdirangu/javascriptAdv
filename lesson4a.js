// 1. Function with no parameters
function greetings() {
  console.log("Hello, World");
}
greetings();

// 2. Function with one parameter
function greet(name) {
  console.log(`Hello, ${name}`);
}
greet("John");

// 3. Function to sum two numbers
function sum(num1, num2) {
  let total = num1 + num2;
  console.log(`The sum is: ${total}`);
}
sum(12, 10);
